digits <- c("-**----*--***--***---*---****--**--****--**---**--",
            "*--*--**-----*----*-*--*-*----*-------*-*--*-*--*-",
            "*--*---*---**---**--****-***--***----*---**---***-",
            "*--*---*--*-------*----*----*-*--*--*---*--*----*-",
            "-**---***-****-***-----*-***---**---*----**---**--",
            "--------------------------------------------------")
numf <- function(s) {
  strsplit(gsub("[^0123456789]", "", s), " ")[[1]]
}

for (line in readLines(tail(commandArgs(), n=1))) {
  cat(sapply(lapply(line, numf), function(s) {
    r <- c("", "", "", "", "", "")
    if (length(s) > 0L) {
      for (i in strsplit(s, "")[[1]]) {
        for (j in 1:6) {
          p <- as.integer(i)*5 + 1
          r[j] <- paste(r[j], substr(digits[j], p, p+4), sep="")
        }
      }
    }
    paste(r, collapse="\n")
  }), sep="\n")
}
