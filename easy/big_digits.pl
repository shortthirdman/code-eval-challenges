use strict;
my @d = (
    '-**----*--***--***---*---****--**--****--**---**--',
    '*--*--**-----*----*-*--*-*----*-------*-*--*-*--*-',
    '*--*---*---**---**--****-***--***----*---**---***-',
    '*--*---*--*-------*----*----*-*--*--*---*--*----*-',
    '-**---***-****-***-----*-***---**---*----**---**--',
    '--------------------------------------------------'
);
my ( $w, $h ) = ( 5, 6 );
open( INFILE, "<", $ARGV[0] )
  or die("Cannot open file $ARGV[0] for reading: $!");
while ( my $line = <INFILE> ) {
    $line =~ tr/0-9//cd;
    for ( my $i = 0 ; $i < $h ; $i++ ) {
        map { print substr( @d[$i], $_ * $w, $w ); } split( //, $line );
        print "\n";
    }
}
close(INFILE);
